export interface Holiday {
  name: string;
  date: Date;
}

function getSpringFestival(year: number): Date {
  const idx = year - 1900;
  if (idx >= 0 && idx < springFestivalData.length) {
    const entry = springFestivalData[idx];
    if (!entry) {
      return new Date(year, 1, 1);
    }
    const [m, d] = entry;
    return new Date(year, m - 1, d);
  }
  return new Date(year, 1, 1);
}

function getDragonBoat(year: number): Date {
  const entry = dragonBoatMap[year];
  if (entry) {
    const [m, d] = entry;
    return new Date(year, m - 1, d);
  }
  return new Date(year, 4, 22);
}

export function getHolidays(year: number): Holiday[] {
  return [
    { name: '元旦', date: new Date(year, 0, 1) },
    { name: '春节', date: getSpringFestival(year) },
    { name: '清明', date: new Date(year, 3, 4) },
    { name: '劳动节', date: new Date(year, 4, 1) },
    { name: '端午', date: getDragonBoat(year) },
    { name: '中秋', date: new Date(year, 8, 15) },
    { name: '国庆', date: new Date(year, 9, 1) },
  ];
}
